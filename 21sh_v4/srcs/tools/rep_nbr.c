#include "./../../vingt_et_un_sh.h"

int		rep_nbr(int x, t_lchar *cmd)
{
	int		y;

	y = x - 1;
	while (cmd[y].type == _NBR && y > 0)
	{
		cmd[y].type = _FD;
		y--;
	}
	return (1);
}
