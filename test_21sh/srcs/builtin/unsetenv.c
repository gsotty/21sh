#include "../../includes/builtin.h"

void	ft_unsetenv(char **cmd, char ***envp)
{
	int		x;

	x = 1;
	while (cmd[x] != NULL)
	{
		remove_env(cmd[x], envp);
		x++;
	}
	return ;
}
