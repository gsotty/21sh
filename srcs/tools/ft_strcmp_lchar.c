#include "./../../vingt_et_un_sh.h"

int	ft_strcmp_lchar(const t_lchar *s1, const char *s2)
{
	int		x;

	x = 0;
	while (s1[x].c == s2[x] && s1[x].c != '\0')
	{
		x++;
	}
	return (s1[x].c - s2[x]);
}
